#faça um programa que leia um numero inteiro e mostre seu sucessor e antecessor
posicao = int(input('digite sua posicao'))
antecessor = posicao-1
sucessor = posicao+1
print('sua posicao é {}, seu antecessor é {}, e seu sucessor é {}!'.format(posicao,antecessor,sucessor))

